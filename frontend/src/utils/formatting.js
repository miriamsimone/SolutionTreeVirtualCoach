export const parseMarkdown = (text) => {
  if (!text) return '';

  // Basic markdown parsing (you might want to use a library like marked.js)
  let html = text
    .replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>')
    .replace(/\*(.+?)\*/g, '<em>$1</em>')
    .replace(/`(.+?)`/g, '<code>$1</code>')
    .replace(/\n/g, '<br>');

  return html;
};
